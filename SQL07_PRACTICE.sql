WHERE (JOB_CODE, 
ABS(EXTRACT(YEAR FROM TO_DATE(SUBSTR(EMP_NO , 1 , 6), 'RRMMDD')) - EXTRACT(YEAR FROM SYSDATE)))
IN (SELECT JOB_CODE,
MIN(ABS(EXTRACT(YEAR FROM TO_DATE(SUBSTR(EMP_NO , 1 , 6), 'RRMMDD')) - EXTRACT(YEAR FROM SYSDATE))) 나이
FROM EMPLOYEE GROUP BY JOB_CODE)
