ABS(EXTRACT(YEAR FROM TO_DATE(SUBSTR(EMP_NO , 1 , 6), 'RRMMDD')) - EXTRACT(YEAR FROM SYSDATE)) 나이
,TO_CHAR((SALARY * 12) + (SALARY * NVL(BONUS, '1') * 12), 'L999,999,999') 연봉
